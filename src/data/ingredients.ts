// Función para obtener ingrediente por ID
export function getExtractedIngredientById(
  id: string
): ExtractedIngredient | undefined {
  return extractedIngredients.find((ing) => ing.id === id);
}

// Función para obtener ingrediente por nombre
export function getExtractedIngredientByName(
  nombre: string
): ExtractedIngredient | undefined {
  const nombreNormalizado = normalizarNombre(nombre);
  return extractedIngredients.find(
    (ing) => normalizarNombre(ing.nombre) === nombreNormalizado
  );
}

// Función para obtener ingredientes por categoría
export function GETExtractedIngredientsByCategory(
  categoria: string
): ExtractedIngredient[] {
  return extractedIngredients.filter((ing) => ing.categoria === categoria);
}

// Función auxiliar para normalizar nombres
function normalizarNombre(nombre: string): string {
  return nombre
    .toLowerCase()
    .trim()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/[^a-z0-9s]/g, " ")
    .replace(/s+/g, " ")
    .trim();
}
